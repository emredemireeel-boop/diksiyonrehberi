function openBlogPost(id) {
  const post = BLOG_POSTS.find(p => p.id === id);
  if (!post) return;
  
  const container = document.getElementById('blog-container');
  if (!container) return;

  // SEO: URL'yi /blog/slug formatına güncelle (history API ile sayfa yenilenmez)
  if (window.location.pathname !== `/blog/${id}`) {
    history.pushState({ blogId: id }, post.title, `/blog/${id}`);
  }
  document.title = post.title + ' - Diksiyon Rehberi';

  window.scrollTo({ top: 0, behavior: 'smooth' });

  // Calculate word count for this article
  let articleWords = post.intro.split(/\s+/).length;
  post.sections.forEach(s => { articleWords += s.content.replace(/<[^>]*>/g, '').split(/\s+/).length; });

  let html = `
    <div class="blog-post">
      <button class="bp-back-btn" onclick="renderBlogList()">← Tüm Makaleler</button>
      
      <div class="bp-hero">
        <div class="bp-hero-emoji">${post.emoji}</div>
        <div class="bp-meta">
          <span class="bp-cat">${post.category}</span>
          <span class="bp-time">⏱ ${post.readTime} okuma</span>
          <span class="bp-time">📅 ${post.date}</span>
          <span class="bp-time">📝 ${articleWords} kelime</span>
        </div>
        <h1 class="bp-title">${post.title}</h1>
        <p class="bp-subtitle">${post.subtitle}</p>
      </div>

      <div class="bp-content">
        <p class="bp-intro">${post.intro}</p>

        <!-- İçindekiler -->
        <div class="bp-toc">
          <div class="bp-toc-title">📋 İçindekiler</div>
          ${post.sections.map((sec, i) => `
            <div class="bp-toc-item">• ${sec.heading.replace(/[^\w\sğüşıöçĞÜŞİÖÇ:&—()""']/g, '').trim()}</div>
          `).join('')}
        </div>
  `;

  post.sections.forEach(sec => {
    html += `
        <div class="bp-section">
          <h2 class="bp-h2">${sec.heading}</h2>
          <div class="bp-sec-body">${sec.content}</div>
        </div>
    `;
  });

  html += `
      </div>

      <div class="bp-cta" onclick="window.location.hash=''; if(typeof setFilter==='function') setFilter('${post.cta.cat}')">
        ${post.cta.text}
      </div>

      <div class="bp-footer">
        <div class="bp-tags">
          ${post.tags.map(t => `<span class="bp-tag">#${t}</span>`).join('')}
        </div>
        <div class="bp-share">
          Bu makaleyi paylaş: 
          <button class="bp-share-btn" onclick="event.stopPropagation(); navigator.clipboard.writeText(window.location.href).then(() => { if(typeof showToast==='function') showToast('Bağlantı kopyalandı!'); })">📋 Bağlantıyı Kopyala</button>
        </div>
      </div>
    </div>
  `;

  container.innerHTML = html;
}

// Global olarak çağrılabilmesi için
window.renderBlogList = renderBlogList;
window.openBlogPost = openBlogPost;

// Browser geri/ileri butonları için history API desteği
window.addEventListener('popstate', (e) => {
  if (e.state && e.state.blogId) {
    openBlogPost(e.state.blogId);
  } else if (window.location.pathname === '/' || window.location.pathname.startsWith('/blog') === false) {
    renderBlogList();
  }
});

// Sayfa /blog/slug URL'iyle doğrudan açıldıysa ilgili postu göster
(function initBlogFromUrl() {
  const match = window.location.pathname.match(/^\/blog\/([^/]+)/);
  if (match) {
    const id = match[1];
    // Blog sekmesini aktif et ve container'ı göster
    const waitForContainer = setInterval(() => {
      const container = document.getElementById('blog-container');
      if (container) {
        clearInterval(waitForContainer);
        // Blog container'ını görünür yap
        container.style.display = '';
        // Blog listesini renderla ve postu aç
        renderBlogList();
        openBlogPost(id);
        // Nav linkini aktif et
        const blogNavLink = document.querySelector('[data-cat="blog"]');
        if (blogNavLink && typeof setFilter === 'function') {
          setFilter('blog');
        }
      }
    }, 100);
  }
})();
