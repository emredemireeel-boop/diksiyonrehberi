function renderBlogList() {
  const container = document.getElementById('blog-container');
  if (!container) return;

  const totalWords = BLOG_POSTS.reduce((sum, p) => {
    let words = p.intro.split(/\s+/).length;
    p.sections.forEach(s => { words += s.content.replace(/<[^>]*>/g, '').split(/\s+/).length; });
    return sum + words;
  }, 0);

  container.innerHTML = `
    <div class="blog-header">
      <span class="blog-header-badge">📰 Eğitim Makaleleri</span>
      <h2>Diksiyon, Etkili İletişim ve Liderlik Sanatı</h2>
      <p>Bilimsel makaleler, nörolojik kanıtlar ve profesyonellerin uyguladığı iletişim yöntemleriyle hayatınızı ve kariyerinizi sesten inşa etmeye başlayın.</p>
      <div class="blog-article-count">
        <strong>${BLOG_POSTS.length}</strong> Makale &nbsp;·&nbsp; <strong>${Math.round(totalWords/1000)}K+</strong> Kelime &nbsp;·&nbsp; Tamamı Ücretsiz
      </div>
    </div>
    
    <div class="blog-grid" id="blog-list-grid">
      ${BLOG_POSTS.map((post, idx) => `
        <article class="blog-card${idx === 0 ? ' featured' : ''}" tabindex="0" onclick="openBlogPost('${post.id}')" style="animation-delay: ${idx * 0.06}s">
          <div class="bc-emoji">${post.emoji}</div>
          <div class="bc-content">
            <div class="bc-meta">
              <span class="bc-cat">${post.category}</span>
              <span class="bc-time">⏱ ${post.readTime} okuma</span>
            </div>
            <h3 class="bc-title">${post.title}</h3>
            <p class="bc-desc">${post.subtitle}</p>
            <div class="bc-tags">
              ${post.tags.slice(0, 3).map(t => `<span class="bc-tag">#${t}</span>`).join('')}
            </div>
            <button class="bc-read-btn">Makaleyi Oku →</button>
          </div>
        </article>
      `).join('')}
    </div>

    <!-- SEO / Stats -->
    <div class="blog-stat-bar">
      <div class="blog-stat-item">
        <span class="blog-stat-num">${BLOG_POSTS.length}</span>
        <span class="blog-stat-label">Makale</span>
      </div>
      <div class="blog-stat-item">
        <span class="blog-stat-num">${BLOG_POSTS.reduce((s,p) => s + p.sections.length, 0)}</span>
        <span class="blog-stat-label">Bölüm</span>
      </div>
      <div class="blog-stat-item">
        <span class="blog-stat-num">${Math.round(totalWords/1000)}K+</span>
        <span class="blog-stat-label">Kelime</span>
      </div>
      <div class="blog-stat-item">
        <span class="blog-stat-num">${new Set(BLOG_POSTS.map(p => p.category)).size}</span>
        <span class="blog-stat-label">Kategori</span>
      </div>
    </div>

    <div class="blog-seo-note" style="margin-top: 20px;">
      💡 <strong>Not:</strong> Düzenli diksiyon pratikleri yaparak sesinizin potansiyelini katlayabilirsiniz. 
      Tüm makalelerimiz bilimsel kaynaklara dayalı, profesyonel diksiyon eğitmenleri tarafından hazırlanmıştır.
    </div>
  `;
}

function openBlogPost(id) {
  const post = BLOG_POSTS.find(p => p.id === id);
  if (!post) return;
  
  const container = document.getElementById('blog-container');
  if (!container) return;

  window.scrollTo({ top: 0, behavior: 'smooth' });

  // Calculate word count for this article
  let articleWords = post.intro.split(/\s+/).length;
  post.sections.forEach(s => { articleWords += s.content.replace(/<[^>]*>/g, '').split(/\s+/).length; });

  let html = `
    <div class="blog-post">
      <button class="bp-back-btn" onclick="renderBlogList()">← Tüm Makaleler</button>
      
      <div class="bp-hero">
        <div class="bp-hero-emoji">${post.emoji}</div>
        <div class="bp-meta">
          <span class="bp-cat">${post.category}</span>
          <span class="bp-time">⏱ ${post.readTime} okuma</span>
          <span class="bp-time">📅 ${post.date}</span>
          <span class="bp-time">📝 ${articleWords} kelime</span>
        </div>
        <h1 class="bp-title">${post.title}</h1>
        <p class="bp-subtitle">${post.subtitle}</p>
      </div>

      <div class="bp-content">
        <p class="bp-intro">${post.intro}</p>

        <!-- İçindekiler -->
        <div class="bp-toc">
          <div class="bp-toc-title">📋 İçindekiler</div>
          ${post.sections.map((sec, i) => `
            <div class="bp-toc-item">• ${sec.heading.replace(/[^\w\sğüşıöçĞÜŞİÖÇ:&—()""']/g, '').trim()}</div>
          `).join('')}
        </div>
  `;

  post.sections.forEach(sec => {
    html += `
        <div class="bp-section">
          <h2 class="bp-h2">${sec.heading}</h2>
          <div class="bp-sec-body">${sec.content}</div>
        </div>
    `;
  });

  html += `
      </div>

      <div class="bp-cta" onclick="window.location.hash=''; if(typeof setFilter==='function') setFilter('${post.cta.cat}')">
        ${post.cta.text}
      </div>

      <div class="bp-footer">
        <div class="bp-tags">
          ${post.tags.map(t => `<span class="bp-tag">#${t}</span>`).join('')}
        </div>
        <div class="bp-share">
          Bu makaleyi paylaş: 
          <button class="bp-share-btn" onclick="event.stopPropagation(); navigator.clipboard.writeText(window.location.href).then(() => { if(typeof showToast==='function') showToast('Bağlantı kopyalandı!'); })">📋 Bağlantıyı Kopyala</button>
        </div>
      </div>
    </div>
  `;

  container.innerHTML = html;
}

